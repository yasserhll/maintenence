function load<T>(key: string, defaults: T[]): T[] {
  const stored = localStorage.getItem(key);
  if (!stored) { localStorage.setItem(key, JSON.stringify(defaults)); return defaults; }
  return JSON.parse(stored);
}
function save<T>(key: string, data: T[]) {
  localStorage.setItem(key, JSON.stringify(data));
}

// ─── Articles ────────────────────────────────────────────
export function getArticles(): Article[] { return load(ARTICLES_KEY, defaultArticles); }
export function saveArticles(a: Article[]) { save(ARTICLES_KEY, a); }
export function addArticle(a: Article) { const list = getArticles(); list.push(a); saveArticles(list); }
export function updateArticle(updated: Article) {
  const list = getArticles();
  const i = list.findIndex(a => a.id === updated.id);
  if (i >= 0) list[i] = updated;
  saveArticles(list);
}
export function deleteArticle(id: string) { saveArticles(getArticles().filter(a => a.id !== id)); }

// ─── Entrées ─────────────────────────────────────────────
export function getEntrees(): MouvementEntree[] { return load(ENTREES_KEY, defaultEntrees); }
export function saveEntrees(e: MouvementEntree[]) { save(ENTREES_KEY, e); }
export function addEntree(e: MouvementEntree) { const list = getEntrees(); list.push(e); saveEntrees(list); }
export function deleteEntree(id: string) { saveEntrees(getEntrees().filter(e => e.id !== id)); }

// ─── Sorties ─────────────────────────────────────────────
export function getSorties(): MouvementSortie[] { return load(SORTIES_KEY, defaultSorties); }
export function saveSorties(s: MouvementSortie[]) { save(SORTIES_KEY, s); }
export function addSortie(s: MouvementSortie) { const list = getSorties(); list.push(s); saveSorties(list); }
export function deleteSortie(id: string) { saveSorties(getSorties().filter(s => s.id !== id)); }

// ─── Stock calculé ───────────────────────────────────────
export function getStockActuel(articleId: string): number {
  const article = getArticles().find(a => a.id === articleId);
  const init    = article?.stockInitial ?? 0;
  const entrees = getEntrees().filter(e => e.articleId === articleId).reduce((s, e) => s + e.quantite, 0);
  const sorties = getSorties().filter(s => s.articleId === articleId).reduce((s, e) => s + e.quantite, 0);
  return init + entrees - sorties;
}

// ─── Inventaires physiques ───────────────────────────────
export function getInventaires(): Inventaire[] { return load(INVENTAIRES_KEY, []); }
export function saveInventaire(inv: Inventaire) {
  const list = getInventaires();
  const i = list.findIndex(x => x.id === inv.id);
  if (i >= 0) list[i] = inv; else list.push(inv);
  save(INVENTAIRES_KEY, list);
}

export function resetToDefaults() {
  [ARTICLES_KEY, ENTREES_KEY, SORTIES_KEY, INVENTAIRES_KEY].forEach(k => localStorage.removeItem(k));
}
